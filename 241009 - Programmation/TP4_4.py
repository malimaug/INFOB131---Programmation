def maximum(nbr1: float, nbr2: float, nbr3: float):
    """The function returns the biggest number of the three inputs

    Parameters
    ----------
    nbr1 : first number (float)
    nbr2 : second number (float)
    nbr3 : third number (float)

    Returns
    -------
    return : the biggest number of the three (float)

    """
    return max(nbr1, nbr2, nbr3)

def minimum(nbr1: float, nbr2: float, nbr3: float):
    """The function returns the smallest number of the three inputs

    Parameters
    ----------
    nbr1 : first number (float)
    nbr2 : second number (float)
    nbr3 : third number (float)

    Returns
    -------
    return : the smallest number of the three (float)

    """
    return min(nbr1, nbr2, nbr3)